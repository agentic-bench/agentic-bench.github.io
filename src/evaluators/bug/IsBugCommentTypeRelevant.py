class IsBugCommentTypeRelevant(BaseEvaluator):
    evaluation_name = "metric/bug/is_bug_comment_type_relevant"

    @staticmethod
    def _get_cwe_group(cwe_id: str) -> str | None:
        """Get the group name for a CWE ID."""
        for group_name, cwes in COMPLETE_CWE_GROUPS.items():
            if cwe_id in cwes:
                return group_name
        return None

    @staticmethod
    def evaluate(comment_dict: dict, ground_truth: list[dict]) -> bool:
        """
        Check if comment's vuln_type matches ground-truth CWE by group.
        
        Two CWEs are considered a match if they belong to the same group.
        
        Args:
            comment_dict: LLM comment with vuln_type field
            ground_truth: List of ground truth records with cwe_id for the same diff
            
        Returns:
            True if any vuln_type group matches any cwe_id group
            False otherwise or if data is missing
        """
        # Get vuln_type from comment
        vuln_type = comment_dict.get("vuln_type", [])
        
        # Handle cases where vuln_type might be None or not a list
        if not vuln_type:
            return False
        
        if not isinstance(vuln_type, list):
            vuln_type = [vuln_type]
        
        # Return False if no ground truth
        if not ground_truth:
            return False
        
        # Get groups for comment vuln_types
        comment_groups = set()
        for vt in vuln_type:
            vt_str = str(vt).strip()
            group = IsBugCommentTypeRelevant._get_cwe_group(vt_str)
            if group:
                comment_groups.add(group)
        
        if not comment_groups:
            return False
        
        # Get groups for ground truth CWEs
        gt_groups = set()
        for gt in ground_truth:
            cwe_id = gt.get("cwe_id")
            if cwe_id:
                # Handle both string and list formats
                cwe_list = cwe_id if isinstance(cwe_id, list) else [cwe_id]
                for cwe in cwe_list:
                    cwe_str = str(cwe).strip()
                    group = IsBugCommentTypeRelevant._get_cwe_group(cwe_str)
                    if group:
                        gt_groups.add(group)
        
        # Check if any groups match
        return bool(comment_groups & gt_groups)
